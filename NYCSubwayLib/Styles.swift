import LUX

public extension UIColor {
    static func primary() -> UIColor {
        return UIColor(hex: 0x4d55a2)
    }
    
    static func primaryDark() -> UIColor {
        return UIColor(hex: 0x333972)
    }
    
    static func accent() -> UIColor {
        return UIColor(hex: 0xdf03a6)
    }
}
